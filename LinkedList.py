from Node import Node


class LinkedList:
    def __init__(self, value) -> None:
        new_node = Node(value)
        self.head = new_node
        self.tail = new_node
        self.length = 1

    def append(self, value) -> bool:
        new_node = Node(value)
        if self.head is None:
            self.head = new_node
            self.tail = new_node
        else:
            self.tail.next = new_node
            self.tail = new_node
        self.length += 1
        return True

    def __str__(self) -> str:
        string = ""
        temp = self.head
        while temp is not None:
            string += f"{temp.value} "
            temp = temp.next

        return string
